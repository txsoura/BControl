    private void btnadicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnadicionarActionPerformed
        if (txtpreco.getText().equals("") || txtcodigo.getText().equals("") || txtquantidade.getText().equals("") || txtnome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo em branco.", "Campo em branco", JOptionPane.WARNING_MESSAGE);
        } else if (txtdesconto.getText().equals("") || Integer.parseInt(txtdesconto.getText()) < 0 || Integer.parseInt(txtdesconto.getText()) > 100) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha um Desconto válido (0 a 100).", "Desconto invalido", JOptionPane.WARNING_MESSAGE);
        } else if (Integer.parseInt(txtquantidade.getText()) <= 0 || Double.parseDouble(txtpreco.getText()) <= 0) {
            txtpreco.setText("");
            JOptionPane.showMessageDialog(null, "Por favor, preencha uma Quantidade e/ou Preço válida.", "Quantidade e/ou Preço invalido", JOptionPane.WARNING_MESSAGE);
        } else {
            Productodao udao = new Productodao();

            try {
                if (udao.cadastrar(txtnome.getText(), Integer.parseInt(txtquantidade.getText()), Double.parseDouble(txtpreco.getText()), txtcodigo.getText(), Byte.parseByte(txtdesconto.getText()))) {
                    this.dispose();
                    Tela tela = new Tela();
                    tela.visualizar();
                } else {
                    JOptionPane.showMessageDialog(null, "Falha ao adicionar.", "Desculpa", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                Logger.getLogger(AdicionarP.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnadicionarActionPerformed

